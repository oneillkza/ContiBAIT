####################################################################################################
#' makeBoxPlot -- creates boxplot of contigs included in the analysis vs those excluded
#' @param chrTable  A GRanges object consisting of contigs and positions. A meta column called 'names' must be present with names in the same
#' format as those from linkage.contigs. Generated by makeChrTable().
#' @param linkage.contigs A list of clustered contigs, generated by clusterContigs()
#' @aliases makeBoxPlot makeBoxPlot-ChrTable-LinkageGroupList-method
#' @rdname makeBoxPlot
#' @example inst/examples/makeBoxPlot.R
#' @return a box plot of included and excluded contigs
#'
#' @export
#'
####################################################################################################


setMethod('makeBoxPlot',
		  signature = signature(chrTable='ChrTable', linkage.contigs='LinkageGroupList'),
		  definition = makeBoxPlot.func
		  )
